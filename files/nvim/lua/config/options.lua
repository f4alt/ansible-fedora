vim.opt.winbar = ""
vim.opt.relativenumber = false

vim.api.nvim_create_autocmd("BufEnter", {
  callback = function()
    vim.opt.formatoptions:remove({ "c", "r", "o" })
  end,
  desc = "Disable New Line Comment",
})

vim.g.minipairs_disable = true
